# list
favfruit=["apple","mango","banana"]
print(favfruit)
#list.append()
favfruit.append("kiwi")
print(favfruit)
#list.insert
favfruit.insert(1,"dragon")
print(favfruit)
#remove
favfruit.remove("dragon")
print(favfruit)
#sort
favfruit.sort()
print(favfruit)
#reverse
favfruit.pop()
print(favfruit)
#pop
favfruit.pop()
print(favfruit)
favfruit.pop(1)
print(favfruit)
